// JS Switch statements
const fruit = 'apple';
switch(fruit) {
    case "bananas":
        console.log("Cameroonian love eating bananas");
        break;
    case "oranges":
        console.log("Oranges are good for our health");
        break;
    case "apple" :
        console.log("Eating one apple per day keeps a doctor far from visiting you.")
        break;
    default:
        console.log(`There's no ${fruit} right now in the fridge.`)
}

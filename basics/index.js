// Arithmetic operator

let a = 10;
let b = 5;
let result = a + b;  // addition operator  +
console.log(result);

let c = 10;
let d = 5;
let e = c - d; // substraction - operator
console.log(e);

let ab = 10;
let bc = 2;
let cd = ab * bc; // Multiplication * operator
console.log(cd);

let m = 10;
let n = 5;
let mn = m / n;  // Division operator
console.log(mn);

let ac = 10; // post increment in Js
ac++;
console.log(ac);

let ay = 10;
--ay; // pre-decrement
console.log(ay);
